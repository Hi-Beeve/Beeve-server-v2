/**
 * ageMin, ageMax → "19~24" 형태의 키로 변환
 */
export function toAgeRangeKey(ageMin: number, ageMax: number): AgeRangeKey {
  return `${ageMin}~${ageMax}` as AgeRangeKey;
}

/**
 * 특정 등급/성별/연령대의 기준값 조회
 */
export function getStandard(
  grade: number,
  gender: string,
  ageMin: number,
  ageMax: number,
): FitnessStandard | undefined {
  const gradeData = PROGRAM_STANDARDS[grade];
  if (!gradeData) return undefined;

  const genderKey = gender as GenderKey;
  const genderData = gradeData[genderKey];
  if (!genderData) return undefined;

  const ageKey = toAgeRangeKey(ageMin, ageMax);
  return genderData[ageKey];
}
